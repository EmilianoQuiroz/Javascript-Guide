//*             ¿Que es Javascript?                */
//**************************************************/
/*
JavaScript es un lenguaje de programación que se utiliza principalmente para aportar dinamismo a sitios y aplicaciones web.
Funciona en complemento con los lenguajes web HTML Y CSS3, permitiendo crear nuevas funcionalidades e interacciones avanzadas con los usuarios, así como interactuar con otras aplicaciones o servicios de backend.

Lenguaje interpretado
A diferencia de un lenguaje de programación compilado, Javascript es un lenguaje interpretado, lo que significa que se ejecuta por medio de un programa intérprete.
En nuestro caso, los navegadores son los programas que se encargan de interpretar y ejecutar el código Javascript que creemos. 
Cuando accedemos a alguna página a través del navegador, éste se encarga de leer y ejecutar todos los archivos que conforman la aplicación (HTML, CSS, JS).

FRONT-END y BACK-END
Javascript se utiliza tanto para construir aplicaciones de Frontend como de Backend. 
Por Frontend entendemos a la parte de la aplicación que corre en el navegador y con la cual interactúan los usuarios. 
Como tal, estaremos creando aplicaciones con Javascript, HTML y CSS; vinculando los tres lenguajes en el desarrollo un único producto.
Nuestra aplicación de Frontend también consume datos y servicios ofrecidos por algún Backend. Javascript será la herramienta que nos permitirá comunicarnos e intercambiar información con APIs u otras aplicaciones.
*/

