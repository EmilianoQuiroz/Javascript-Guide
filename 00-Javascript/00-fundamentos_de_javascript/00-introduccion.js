/*
Sintaxis: reglas básicas
Cuando escribimos código JavaScript, hay que tener en cuenta que no importa la cantidad de espacios en blanco que ingresemos, pero tenemos que tener especial cuidado en respetar las mayúsculas y minúsculas al tipear los elementos a utilizar. JavaScript es un lenguaje case sensitive porque distingue entre mayúsculas y minúsculas. Es decir, no es lo mismo escribir “UNO” que “uno”: se consideran diferentes. En comparación, HTML es un lenguaje case insensitive, donde <p> y <P> se entienden iguales.
Como desarrolladores y al igual que en otros lenguajes, también podemos insertar comentarios en nuestro código. En JS podemos agregar comentarios de una sóla línea con la combinación “//”, o comentarios de bloque, de varias líneas, envolviendo el comentario con los signos /* */

 	// comentario de una línea
