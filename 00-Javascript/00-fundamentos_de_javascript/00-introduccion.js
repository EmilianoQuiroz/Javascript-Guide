//*            Historia de Javascript              */
//**************************************************/
/*
¿CUÁNDO NACE?
La primera aparición pública de Javascript la encontramos en el año 1995 cuando se utiliza como herramienta del navegador Netscape Navigator.
El objetivo fue permitir agregar programas a páginas web.

EVOLUCIÓN DE JAVASCRIPT
La primera versión de JavaScript ES1 se lanzó en 1997 y el lenguaje fue cambiando con el tiempo. En el curso nos focalizamos en las versiones ES6 y posteriores. Actualmente JS se encuentra en la versión 11 y desarrollo de la 12.

DIFERENCIA CON JAVA
Java funciona a través de declaraciones que se establecen a partir de un sistema de clases en tiempo de compilación. 
En cambio, JavaScript se basa en un sistema de tiempo de ejecución donde cada dato distinto puede representar valores booleanos, de cadena o numéricos.
*/

